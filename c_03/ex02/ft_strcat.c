char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	j = 0;
	while (*(dest + j))
		j++;
	i = 0;
	while (*(src + i))
	{
		*(dest + j) = *(src + i);
		i ++;
		j ++;
	}
	*(dest + j) = 0;
	return (dest);
}
