int	ft_str_is_printable(char *str)
{
	int	n;
	int	i;

	i = 0;
	while (*(str + i))
	{
		n = str[i];
		if (!(' ' <= n && n <= '~'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
