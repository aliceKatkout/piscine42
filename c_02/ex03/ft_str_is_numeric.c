int	ft_str_is_numeric(char *str)
{
	int	n;
	int	i;

	i = 0;
	while (*(str + i))
	{
		n = str[i];
		if (!('0' <= n && n <= '9'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
