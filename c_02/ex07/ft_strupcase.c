char	*ft_strupcase(char *str)
{
	int	n;
	int	i;

	i = 0;
	while (*(str + i))
	{
		n = str[i];
		if ('a' <= n && n <= 'z')
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
