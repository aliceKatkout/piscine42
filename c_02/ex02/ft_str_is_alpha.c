int	ft_str_is_alpha(char *str)
{	
	int	n;
	int	i;

	i = 0;
	while (*(str + i))
	{
		n = str[i];
		if (!(('a' <= n && n <= 'z') || ('A' <= n && n <= 'Z')))
		{
			return (0);
		}
		i++;
	}	
	return (1);
}
